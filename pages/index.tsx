import Head from "next/head";
import Banner from "../components/banner/banner";
import SectionCards from "../components/card/section-cards";
import NavBar from "../components/nav/navbar";
import styles from "../styles/Home.module.css";
import { getPopularVideos, getVideos } from "../lib/videos";
import { GetServerSideProps, InferGetServerSidePropsType } from "next";

export interface VideosProps {
  title: string;
  imgUrl: string;
  id?: string;
}

export const getServerSideProps: GetServerSideProps = async () => {
  const disneyVideos: VideosProps[] = await getVideos("marvel");
  const productivityVideos: VideosProps[] = await getVideos("productivity");
  const travelVideos: VideosProps[] = await getVideos("the DoDo Men");
  const popularVideos: VideosProps[] = await getPopularVideos();

  return { props: { disneyVideos, productivityVideos, travelVideos, popularVideos } };
};

export default function Home({ disneyVideos, productivityVideos, travelVideos, popularVideos }: InferGetServerSidePropsType<typeof getServerSideProps>) {
  return (
    <div className={styles.container}>
      <Head>
        <title>Netflix Tutorial</title>
        <link rel="icon" href="/favicon.ico" />
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>

      <div className={styles.main}>
        <NavBar username="Ze Yen" />

        <Banner
          title="Clifford the red dog"
          subTitle="a very cute dog"
          imgUrl="/static/clifford.webp"
        />

        <br />
        <h1>This is localhost, chill out</h1>

        <div className={styles.sectionWrapper}>
          <SectionCards title="Disney" videos={disneyVideos} size="large" />
          <SectionCards title="Travel" videos={travelVideos} size="small" />
          <SectionCards title="Productivity" videos={productivityVideos} size="medium" />
          <SectionCards title="Popular" videos={popularVideos} size="small" />
        </div>
      </div>
    </div>
  );
}
